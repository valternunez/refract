/**
 * Token-footprint benchmark: how many tokens does a `render_responsive` response
 * cost an agent? Renders a page and compares three response shapes —
 *   findings-only · downscaled previews (what Refract sends) · full-res (naive) —
 * across the Claude / GPT-4o / Gemini image tokenizers, then writes RESULTS.md.
 *
 * Usage: node benchmarks/tokens.mjs [url] [viewports]
 *   defaults: the bundled demo-site at mobile,tablet,desktop.
 */

import { mkdtemp, rm, writeFile } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { render } from '@getrefractjs/core';
import { encode } from 'gpt-tokenizer/encoding/o200k_base';
import sharp from 'sharp';
import { IMAGE_TOKENS } from './image-tokens.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const DEMO = pathToFileURL(join(here, '../examples/demo-site/index.html')).href;
const PREVIEW_MAX_WIDTH = 800; // mirrors packages/mcp/src/tool.ts downscalePreview

const url = process.argv[2] || DEMO;
const viewports = (process.argv[3] || 'mobile,tablet,desktop').split(',');
const MODELS = Object.keys(IMAGE_TOKENS);

const tokens = (text) => encode(text).length;
const also = (s) => (s.aliases?.length ? ` [also: ${s.aliases.join(', ')}]` : '');

async function dims(buf) {
  const { width, height } = await sharp(buf).metadata();
  return { width, height };
}

/** Mirror downscalePreview: cap width at 800, else return the image untouched. */
async function downscale(buf) {
  const { width } = await sharp(buf).metadata();
  if (!width || width <= PREVIEW_MAX_WIDTH) return buf;
  return sharp(buf).resize({ width: PREVIEW_MAX_WIDTH }).png().toBuffer();
}

const outDir = await mkdtemp(join(tmpdir(), 'refract-bench-'));
let report;
try {
  const shots = await render({ url, viewports, out: outDir, freeze: true });

  // The exact text the MCP response carries (see renderResponsive()).
  const manifest = [
    `Rendered ${url} at ${shots.length} unique render(s). Full-resolution PNGs saved to:`,
    ...shots.map((s) => `- ${s.preset} (${s.width}×${s.height}) → ${s.savedPath}${also(s)}`),
  ].join('\n');
  const findingsText = `Findings:\n${JSON.stringify(
    shots.map((s) => ({ preset: s.preset, aliases: s.aliases, findings: s.findings })),
    null,
    2,
  )}`;
  const labelsText = shots.map((s) => `${s.preset} (${s.width}×${s.height})${also(s)}:`).join('\n');

  const textFindingsOnly = tokens(manifest) + tokens(findingsText);
  const textWithImages = textFindingsOnly + tokens(labelsText);

  // Per shot: full-res pixel dims and the downscaled-preview pixel dims.
  const perShot = [];
  for (const s of shots) {
    const full = await dims(s.image);
    const preview = await dims(await downscale(s.image));
    perShot.push({ preset: s.preset, full, preview });
  }

  const sumImage = (model, pick) =>
    perShot.reduce((n, p) => n + IMAGE_TOKENS[model](pick(p).width, pick(p).height), 0);

  const modes = {
    'findings-only': { text: textFindingsOnly, image: () => 0 },
    downscaled: { text: textWithImages, image: (m) => sumImage(m, (p) => p.preview) },
    'full-res': { text: textWithImages, image: (m) => sumImage(m, (p) => p.full) },
  };

  // total[mode][model]
  const total = {};
  for (const [mode, m] of Object.entries(modes)) {
    total[mode] = {};
    for (const model of MODELS) total[mode][model] = m.text + m.image(model);
  }

  const pad = (s, n) => String(s).padEnd(n);
  const padL = (s, n) => String(s).padStart(n);
  const headline = `For ${shots.length} viewport(s) of ${url === DEMO ? 'the demo-site' : url}: a render_responsive response costs ~${total.downscaled.claude.toLocaleString()} tokens (downscaled, Claude) vs ~${total['full-res'].claude.toLocaleString()} full-res — ${Math.round((1 - total.downscaled.claude / total['full-res'].claude) * 100)}% smaller. The findings JSON alone is ~${total['findings-only'].claude.toLocaleString()}.`;

  // Console table.
  console.log(`\n${headline}\n`);
  console.log(`${pad('mode', 16)}${padL('text', 8)}${MODELS.map((m) => padL(m, 12)).join('')}`);
  for (const mode of Object.keys(modes)) {
    console.log(
      pad(mode, 16) +
        padL(modes[mode].text.toLocaleString(), 8) +
        MODELS.map((m) => padL(total[mode][m].toLocaleString(), 12)).join(''),
    );
  }

  // RESULTS.md (committed).
  const mdRow = (mode) =>
    `| ${mode} | ${modes[mode].text.toLocaleString()} | ${MODELS.map((m) => total[mode][m].toLocaleString()).join(' | ')} |`;
  const dimsRows = perShot
    .map(
      (p) =>
        `| ${p.preset} | ${p.full.width}×${p.full.height} | ${p.preview.width}×${p.preview.height} |`,
    )
    .join('\n');

  report = `# Token footprint

> Generated by \`pnpm bench\` (\`benchmarks/tokens.mjs\`). Re-run to refresh.

**${headline}**

Page: \`${url === DEMO ? 'examples/demo-site/index.html' : url}\` · viewports: \`${viewports.join(', ')}\`

Tokens an agent pays for one \`render_responsive\` response, by response shape and model:

| mode | text | ${MODELS.join(' | ')} |
|---|---|${MODELS.map(() => '---').join('|')}|
${mdRow('findings-only')}
${mdRow('downscaled')}
${mdRow('full-res')}

- **findings-only** — manifest + findings JSON, no images (text only).
- **downscaled** — what Refract returns: previews capped at ${PREVIEW_MAX_WIDTH}px wide. *(default)*
- **full-res** — what a naive screenshot tool would dump.

Per-viewport image dimensions (full-resolution vs downscaled preview):

| preset | full-res | preview |
|---|---|---|
${dimsRows}

### How it's counted

Text is tokenized with \`gpt-tokenizer\` (o200k_base); it's a proxy for Claude/Gemini
text (within ~10–20%), which is fine because images dominate the total. Base64 image
data is **not** counted as text — the MCP sends images as image content blocks, so they
cost *image* tokens per each model's published formula:

- **Claude:** \`ceil(w/28) * ceil(h/28)\` — [docs](https://platform.claude.com/docs/en/build-with-claude/vision)
- **GPT-4o (high detail):** fit to 2048², scale shortest side to 768, then \`85 + 170 * tiles\` (512px tiles) — [docs](https://platform.openai.com/docs/guides/images)
- **Gemini 2.0:** \`258\` if ≤384², else \`ceil(w/768) * ceil(h/768) * 258\` — [docs](https://ai.google.dev/gemini-api/docs/tokens)

These are estimates, not billed counts; they exist to show the *relative* cost of
Refract's findings-first, downscaled responses versus dumping full-resolution images.
`;
  await writeFile(join(here, 'RESULTS.md'), report);
  console.log(`\nwrote ${join(here, 'RESULTS.md')}`);
} finally {
  await rm(outDir, { recursive: true, force: true });
}
